use v5.36;

sub centroid ($LoL) {
    my $n = $#{ $LoL       };
    my $d = $#{ $LoL->@[0] };
    my @C = 0 x ($d+1);
    for my $i (0..$d) {
        $C[$i] += $LoL->@[$_]->@[$i] for 0..$n;
        $C[$i] /= $n+1
    }
    @C
}

say join ' ', centroid($_) for
    [ [1,], [2,], [3,] ],
    [ [8, 2], [0, 0] ],
    [ [5, 5, 0], [10, 10, 0] ],
    [ [1, 3.1, 6.5], [-2, -5, 3.4], [-7, -4, 9], [2, 0, 3] ],
    [ [0, 0, 0, 0, 1], [0, 0, 0, 1, 0], [0, 0, 1, 0, 0], [0, 1, 0, 0, 0] ];
