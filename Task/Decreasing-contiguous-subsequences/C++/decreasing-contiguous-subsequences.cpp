int main() {
	const std::vector<uint32_t> bins = { 0, 4, 8, 12, 16, 25, 100 };
	std::vector<uint32_t> results(bins.size() - 1, 0);
	std::vector<double> gradePercents;

	struct Pair {
		uint32_t x;
		uint32_t y;
	};

	Pair pair(sequence[0], sequence[0]);

	for ( uint32_t i = 1; i < sequence.size(); ++i ) {
		if ( sequence[i] > pair.y ) {
			if ( pair.x != pair.y ) {
				gradePercents.emplace_back(100.0 * (double) ( pair.x - pair.y ) / pair.x);
			}
			pair = Pair(sequence[i], sequence[i]);
		} else {
			pair.y = sequence[i - 1];
		}
	}

	for ( uint32_t i = 0; i < bins.size() - 1; ++i ) {
		for ( const double& grade : gradePercents ) {
			if ( grade >= bins[i] && grade < bins[i + 1] ) {
				results[i]++;
			}
		}
	}

	std::cout << "  Interval   Count" << std::endl;
	std::cout << "===================" << std::endl;
	for ( uint32_t i = 0; i < bins.size() - 1; ++i ) {
		std::cout << " [" << std::setw(2) << bins[i] << "% - " << std::setw(3) << bins[i + 1]
				  << "%)   " << results[i] << std::endl;
	}
}
