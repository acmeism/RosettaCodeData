use strict;
use warnings;

my @lists = (
    [ < 9 3 3 3 2 1 7 8 5 > ],
    [ < 5 2 9 3 3 7 8 4 1 > ],
    [ < 1 4 3 6 7 3 8 3 2 > ],
    [ < 1 2 3 4 5 6 7 8 9 > ],
    [ < 4 6 8 7 2 3 3 3 1 > ],
    [ < 3 3 3 1 2 4 5 1 3 > ],
    [ < 0 3 9 3 3 7 2 2 6 > ],
    [ < 3 3 3 3 3 4 4 4 4 > ],
);

print ' 'x21 . '0x0 1x1 2x2 3x3 4x4' . "\n";
for my $ref ( @lists ) {
    print "@$ref: ";
    for my $n (0..4) {
        my @i = grep $ref->[$_] == $n, 0 .. $#$ref;
        print '   ', $n==0 && !@i || @i == $n && ($n==1 || ($n-1 == grep $i[$_-1]+1 == $i[$_], 1..$n-1)) ? 'Y' : 'N';
    }
    print "\n";
}
