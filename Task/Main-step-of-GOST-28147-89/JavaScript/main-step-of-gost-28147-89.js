const Основной_шаг = (блок_текста, элемент_ключа, ТЗ) => {
	const
		N = блок_текста.slice(0),
		S = N[0] + элемент_ключа & 0xFFFFFFFF;
	let нов_S = 0;
	for (let сч = 0; сч < 4; сч++) {
		const яч = (S >>> (сч << 3)) & 0xFF;
		нов_S +=
			ТЗ[сч * 2][яч & 0x0F]
			+ (ТЗ[сч * 2 + 1][яч >>> 4] << 4)
			<< (сч << 3);
	}
	нов_S =
		(нов_S << 11)
		+ (нов_S >>> 21)
		& 0xFFFFFFFF
		^ N[1];
	N[1] = N[0]; N[0] = нов_S;
	return N;
};
